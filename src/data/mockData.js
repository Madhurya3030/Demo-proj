// Programmatically generate 90 homepage products (10 products * 9 categories)
export const getHomepageProducts = () => {
  const sections = [
    { key: "trending", name: "Trending Products", prefix: "Trending" },
    { key: "recommended", name: "Recommended For You", prefix: "Special" },
    { key: "best_sellers", name: "Best Sellers", prefix: "Popular" },
    { key: "deals_under_499", name: "Deals Under ₹499", prefix: "Budget" },
    { key: "womens_fashion", name: "Women's Fashion", prefix: "Designer" },
    { key: "jewellery", name: "Jewellery", prefix: "Classic" },
    { key: "home_essentials", name: "Home Essentials", prefix: "Utility" },
    { key: "beauty", name: "Beauty", prefix: "Organic" },
    { key: "electronics", name: "Electronics", prefix: "Smart" }
  ];

  const products = [];
  let idCounter = 1;

  sections.forEach((sec) => {
    for (let i = 1; i <= 10; i++) {
      const price = sec.key === "deals_under_499" ? Math.floor(Math.random() * 200) + 199 : Math.floor(Math.random() * 800) + 299;
      const originalPrice = Math.floor(price * (Math.random() * 0.6 + 1.4));
      const discount = Math.round(((originalPrice - price) / originalPrice) * 100);
      const rating = (Math.random() * 1.0 + 3.8).toFixed(1);
      const images = homeImages[sec.key];
      const image = images[Math.floor(Math.random() * images.length)];

      products.push({
        id: idCounter++,
        section: sec.key,
        sectionName: sec.name,
        name: `${sec.prefix} ${sec.key.replace("_", " ")} Item #${i}`,
        price,
        originalPrice,
        discount,
        rating,
        image,
        freeDelivery: Math.random() > 0.15
      });
    }
  });

  return products;
};

